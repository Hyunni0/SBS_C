#include <stdio.h>

// 전역 변수
// : 프로그램의 모든 영역에서 접근 가능
int number = 100;

void printNumber(void){
    printf("전역 변수 number는 %d \n", number);
}

int main(void){
    // 지역 변수
    // : 선언된 중괄호 { } 블록 안에서만 접근 가능
    int number = 200;
    printf("지역 변수 number는 %d \n", number);

    if( number > 10 ){
        int number = 300;
        printf("(조건문) 지역 변수 number 는 %d \n", number);
    }

    // 함수 호출
    printNumber();

    return 0;

}